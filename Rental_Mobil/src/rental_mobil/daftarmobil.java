    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
        // TODO add your handling code here:
        int Kodepinjam = 0;
        int lama = Integer.parseInt(lamapinjem.getText());
        int harga = Integer.parseInt(tarif.getText());
        int mobil = Integer.parseInt(kodemobil.getText());
        SimpleDateFormat formattgl = new SimpleDateFormat("yyyy-MM-dd");
        pinjam = formattgl.format(tglpinjam.getDate());
        String booking = "Dipinjam";
        String status = null;
        
     
        try {
            stm = Con.createStatement();
            RsMobil = stm.executeQuery("select * from mobil where idm = "+mobil+"");
            RsMobil.beforeFirst();
            while(RsMobil.next()) {
                status = RsMobil.getString("mobil.status");
            }
            
            if("Dipinjam".equals(status)){JOptionPane.showMessageDialog(null, "Mobil Sedang Dipinjam");}
            else{
                    stm.executeUpdate("INSERT into pelanggan VALUES("+nik.getText()+",'"+namapenyewa.getText()+"','"+alamat.getText()+"',"+hp.getText()+")");
                    stm.executeUpdate("INSERT into pinjam VALUES("+Kodepinjam+",'"+pinjam+"','"+nik.getText()+"',"+lama+","+harga+","+mobil+")");
                    stm.executeUpdate("Update mobil set status = '"+booking+"' where idm = "+mobil+"");
                    kosong();
                    kosong_mobil();
                    JOptionPane.showMessageDialog(null, "Berhasil di Update");
            }
        } catch (SQLException ex) {
            Logger.getLogger(daftarmobil.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_simpanActionPerformed

