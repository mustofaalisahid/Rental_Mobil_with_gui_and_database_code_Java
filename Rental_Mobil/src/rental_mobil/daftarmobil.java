    private void livinaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_livinaActionPerformed
        // TODO add your handling code here:
        String status;
        try {
            kosong_mobil();
            String Mobil = "Grand Livina";
            stm = Con.createStatement();
            RsMobil = stm.executeQuery("select * from mobil where type = '"+Mobil+"'");
            RsMobil.beforeFirst();
            while(RsMobil.next()) {
                status = RsMobil.getString("status");
                if("Rusak".equals(status)){
                    JOptionPane.showMessageDialog(null, "Mobil Sedang Service");
                }
                else{
                    kodemobil.setText(RsMobil.getString("idm"));
                    typemobil.setText(RsMobil.getString("type"));
                    platnomer.setText(RsMobil.getString("plat"));
                    harga.setText(RsMobil.getString("harga"));
                }
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_livinaActionPerformed

